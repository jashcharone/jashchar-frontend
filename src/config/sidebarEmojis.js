/**
 * Get emoji for a menu item title
 * @param {string} title - Menu item title
 * @returns {string} Emoji character or empty string
 */
export const getMenuEmoji = (title) => {
  if (!title) return '';
  // Exact match first
  if (MENU_EMOJIS[title]) return MENU_EMOJIS[title];
  // Try partial match
  const normalizedTitle = title.toLowerCase();
  for (const [key, emoji] of Object.entries(MENU_EMOJIS)) {
    if (normalizedTitle.includes(key.toLowerCase())) {
      return emoji;
    }
  }
  return '';
};
